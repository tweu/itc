def add(firstplus, secondplus):
	return firstplus + secondplus

res = add(10, 5)
print('Плюс: ', res)

def subtract(firstminus, secondminus):
	return firstminus - secondminus

print('Минус: ', subtract(10, 5))

def multiply(firstmultiply, secondmultiply):
	return firstmultiply * secondmultiply

print('Умножить: ', multiply(10 ,5 ))


def divide(firstdivide, seconddivide):
	return firstdivide / seconddivide

print('Делить: ', divide(10, 5))